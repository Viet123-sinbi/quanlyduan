package Login;

public class LOGIN1 {
String Username;
String Password;


public LOGIN1( String username, String password) {
	super();
	Username = username;
	Password = password;
}

public String getUsername() {
	return Username;
}


public void setUsername(String username) {
	Username = username;
}


public String getPassword() {
	return Password;
}


public void setPassword(String password) {
	Password = password;
}



}
